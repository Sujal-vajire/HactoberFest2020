# Python Program to Find Area of a Circle with Given Radius

def findAreaCircle(r): 
	pi = 3.14
	return pi*(r*r); 

# Enter value of r in findAreaCircle(r) to get area of circle of given radius. 
print("Area of Circle is is %.4f" % findAreaCircle(8)); 

